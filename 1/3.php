<?php

function sumar($n1, $n2)
{

    $operacion = $n1 + $n2;

    return $operacion;
}

function restar($n1, $n2)
{

    $operacion = $n1 - $n2;

    return $operacion;
}

function multiplicar($n1, $n2)
{

    $operacion = $n1 * $n2;

    return $operacion;
}

function dividir($n1, $n2)
{

    $operacion = $n1 / $n2;

    return $operacion;
}
